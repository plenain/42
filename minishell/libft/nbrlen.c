int		nbrlen(long long n)
{
	int l;

	l = (n <= 0);
	while (n != 0)
	{
		n /= 10;
		++l;
	}
	return (l);
}

int		unbrlen(unsigned long long n)
{
	int l;

	l = (n <= 0);
	while (n != 0)
	{
		n /= 10;
		++l;
	}
	return (l);
}
