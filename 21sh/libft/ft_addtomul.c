#include "libft.h"

int	ft_addtomul(int a, int c)
{
	int	b;
	int g;

	g = c;
	b = a * c;
	while (c--)
		g = g + c;
	if (!(g == b) && (g = a))
		while (a--)
			g = g + a;
	return (g == b);
}
